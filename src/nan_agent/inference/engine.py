@dataclass
class EngineStats:
    """Engine statistics snapshot.

    Attributes:
        total_nodes: Total number of nodes in the graph.
        active_nodes: Number of active (non-pruned) nodes.
        total_edges: Total number of edges in the graph.
        pool_size: Number of nodes in the active pool.
        action_queue_size: Number of pending actions.
        steps_run: Total steps executed.
        nodes_processed: Total nodes processed by the scheduler.
        pruned_nodes: Total nodes pruned.
        dmn_generated: Total nodes generated by DMN.
        uptime_seconds: Engine uptime in seconds.
        graph_entropy: Structural entropy of the graph (0.0-1.0).
        total_activations: Sum of all node activation energies.
        growth_strategy: Name of the current DMN growth strategy.
    """
    total_nodes: int = 0
    active_nodes: int = 0
    total_edges: int = 0
    pool_size: int = 0
    action_queue_size: int = 0
    steps_run: int = 0
    nodes_processed: int = 0
    pruned_nodes: int = 0
    dmn_generated: int = 0
    uptime_seconds: float = 0.0
    # ── 新增字段 ──
    graph_entropy: float = 0.0       # 图的结构熵
    total_activations: float = 0.0   # 所有节点激活能量总和
    growth_strategy: str = ""        # 当前 DMN 增长策略名称


class GoTEngine:
    """Graph of Thought 推理引擎

    NAN-Agent 的核心推理组件，整合图推理、记忆、元认知、工具调用和
    画图思考等能力，构成一个持续运行的自主推理循环。

    生命周期模型：
    引擎以统一的 _lifecycle 循环运行，CEN 只是另一种输入源，
    无需显式模式切换。每个 tick：
    1. 消费外部输入（CEN、感知、工具返回）
    2. 若节点池非空，调度器处理批次
    3. 若节点池为空，DMN 自发生成节点
    4. 扩散激活传播 + 全局衰减
    5. 周期性任务（自动保存、记忆反思）
    6. 等待 tick 间隔

    持久化：
    图可通过 save_graph / load_graph 序列化到 JSON 文件，
    并支持自动保存和启停时自动加载/保存。

    TA 集成 (TA ↔ GoT):
    - receive_from_ta(): TA 向 GoT 图注入新节点
    - _dispatch_actions(): GoT 将动作分发到 TA 执行
    - _consume_ta_push(): 消费 TA 的消息队列
    """

    def __init__(
        self,
        cognition,
        hard_memory=None,
        self_value=None,
        soft_memory=None,
        web_search=None,
        code_executor=None,
        config=None,
        task_agent_factory=None,
        embedding_provider=None,
    ):
        self._cognition = cognition
        self._hard_memory = hard_memory
        self._self_value = self_value
        self._soft_memory = soft_memory
        self._config = config or {}

        # ── Tick 间隔配置 ──
        self._tick_interval: float = self._config.get("tick_interval", 1.0)

        # ── 自动保存配置 ──
        self._auto_save_interval: int = self._config.get("auto_save_interval", 50)
        self._graph_save_path: Optional[str] = self._config.get("graph_save_path", None)
        self._steps_since_last_save: int = 0

        # ── 批次大小 ──
        self._batch_size: int = self._config.get("batch_size", 5)

        # ── 核心组件 ──
        self.graph = GoTGraph()
        self.pool = NodePool()
        self.action_queue: list[dict] = []

        self._meta = MetaCognition(cognition=cognition, embedding_provider=embedding_provider)
        self._draw_of_thought = DrawOfThought(cognition=cognition)

        self._tools = GoTToolkit(
            cognition=cognition,
            hard_memory=hard_memory,
            web_search=web_search,
            code_executor=code_executor,
        )

        self._dmn = SpontaneousGrowthStrategy(
            cognition=cognition,
            hard_memory=hard_memory,
            graph=self.graph,
            min_pool_size=self._config.get("dmn_min_pool_size", 3),
            config=self._config.get("dmn_config", {}),
            embed_fn=embedding_provider,
        )

        self._reasoning_loop = ReasoningLoop(
            cognition=cognition,
            graph=self.graph,
            hard_memory=hard_memory,
            tools=self._tools,
            meta=self._meta,
            action_queue=self.action_queue,
            draw_of_thought=self._draw_of_thought,
            self_value=self_value,
            soft_memory=soft_memory,
            embed_fn=embedding_provider,
        )

        self._scheduler = GoTScheduler(
            cognition=cognition,
            graph=self.graph,
            pool=self.pool,
            reasoning_loop=self._reasoning_loop,
        )



        # ── 运行状态 ──
        self._running = False
        self._start_time = 0.0
        self._steps_run = 0
        self._nodes_processed = 0
        self._dmn_generated = 0
        self._pruned_nodes = 0
        self._event_bus = None
        self._perception = None



        # ── TA 集成 ──
        self._task_agent_factory = task_agent_factory
        self._pending_tasks: dict[str, asyncio.Task] = {}
        self._ta_push_queue: list[dict] = []  # TA → GoT 的消息队列

        # ── 后台任务引用 ──
        self._lifecycle_task: Optional[asyncio.Task] = None

    # ═══════════════════════════════════════════════════════════
    # 图持久化 (Graph Persistence)
    # ═══════════════════════════════════════════════════════════

    def save_graph(self, path: str) -> None:
        """Serialize the graph to a JSON file.

        Args:
            path: Absolute or relative file path for the JSON output.
        """
        try:
            data = self.graph.to_dict()
            # 确保目录存在
            Path(path).parent.mkdir(parents=True, exist_ok=True)
            with open(path, "w", encoding="utf-8") as f:
                json.dump(data, f, ensure_ascii=False, indent=2)
            log_event(logger, "got_graph_saved", path=path, nodes=self.graph.node_count)
        except Exception as e:
            logger.error("got_graph_save_failed", path=path, error=str(e))

    def load_graph(self, path: str) -> None:
        """Load the graph from a JSON file.

        Replaces the current graph and re-populates the node pool
        with all active (non-pruned) nodes from the loaded graph.

        Args:
            path: Absolute or relative file path for the JSON input.
        """
        try:
            with open(path, "r", encoding="utf-8") as f:
                data = json.load(f)
            self.graph = GoTGraph.from_dict(data)
            # 重建节点池：将所有活跃节点加入池中
            self.pool = NodePool()
            for node in self.graph.get_active_nodes():
                self.pool.add(node)
            # 重新绑定 DMN 的图引用
            self._dmn._graph = self.graph
            # 重新绑定推理循环和调度器的图引用
            self._reasoning_loop._graph = self.graph
            self._scheduler._graph = self.graph
            log_event(
                logger, "got_graph_loaded",
                path=path, nodes=self.graph.node_count,
                active=self.graph.active_count,
            )
        except FileNotFoundError:
            logger.warning("got_graph_load_not_found", path=path)
        except Exception as e:
            logger.error("got_graph_load_failed", path=path, error=str(e))

