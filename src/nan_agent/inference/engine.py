    async def health_check(self) -> bool:
        """Run a health check on all engine components.

        Returns:
            True if all components are healthy, False otherwise.
        """
        try:
            cog_ok = await self._cognition.health_check()
        except Exception:
            cog_ok = False

        graph_ok = self.graph is not None
        pool_ok = self.pool is not None
        scheduler_ok = self._scheduler is not None
        reasoner_ok = self._reasoning_loop is not None
        meta_ok = self._meta is not None
        tools_ok = self._tools is not None
        dmn_ok = self._dmn is not None
        dwt_ok = self._draw_of_thought is not None

        all_ok = all([
            cog_ok, graph_ok, pool_ok, scheduler_ok,
            reasoner_ok, meta_ok, tools_ok, dmn_ok, dwt_ok,
        ])

        if not all_ok:
            failed = []
            if not cog_ok:
                failed.append("cognition")
            if not graph_ok:
                failed.append("graph")
            if not pool_ok:
                failed.append("pool")
            if not scheduler_ok:
                failed.append("scheduler")
            if not reasoner_ok:
                failed.append("reasoning_loop")
            if not meta_ok:
                failed.append("metacognition")
            if not tools_ok:
                failed.append("tools")
            if not dmn_ok:
                failed.append("dmn")
            if not dwt_ok:
                failed.append("draw_of_thought")
            logger.warning("Health check failed", failed=(", ".join(failed)))

        return all_ok
