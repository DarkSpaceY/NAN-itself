    # 统计与查询
    # ═══════════════════════════════════════════════════════════

    def get_stats(self) -> EngineStats:
        """Get current engine statistics.

        Returns:
            An EngineStats snapshot including graph entropy, total
            activations, and current growth strategy name.
        """
        # 计算图的结构熵
        graph_entropy = 0.0
        try:
            graph_entropy = self.graph.structural_entropy()
        except Exception as e:
            logger.warning("got_stats_entropy_failed", error=str(e))

        # 计算所有节点的激活能量总和
        total_activations = 0.0
        try:
            total_activations = sum(n.activation for n in self.graph._nodes.values())
        except Exception as e:
            logger.warning("got_stats_activations_failed", error=str(e))

        # 获取当前 DMN 增长策略名称
        growth_strategy_name = ""
        try:
            # SpontaneousGrowthStrategy 内部维护策略权重，取权重最高的策略
            if hasattr(self._dmn, '_strategy_weights') and self._dmn._strategy_weights:
                best = max(self._dmn._strategy_weights.items(), key=lambda x: x[1])
                growth_strategy_name = best[0].value
        except Exception as e:
            logger.debug("got_stats_strategy_name_failed", error=str(e))

        return EngineStats(
            total_nodes=self.graph.node_count if self.graph else 0,
            active_nodes=self.graph.active_count if self.graph else 0,
            total_edges=len(self.graph._edges) if self.graph else 0,
            pool_size=self.pool.size() if self.pool else 0,
            action_queue_size=len(self.action_queue),
            steps_run=getattr(self, '_steps_run', 0),
            nodes_processed=getattr(self, '_nodes_processed', 0),
            pruned_nodes=self.graph.node_count - self.graph.active_count if self.graph else 0,
            dmn_generated=getattr(self, '_dmn_generated', 0),
            uptime_seconds=time.time() - self._start_time if self._start_time > 0 else 0.0,
            # ── 新增字段 ──
            graph_entropy=graph_entropy,
            total_activations=total_activations,
            growth_strategy=growth_strategy_name,
        )

    def get_action_outputs(self) -> list:
        """Get all nodes that have pending action outputs."""
        return [
            n for n in self.pool.nodes.values()
            if hasattr(n, 'action_output') and n.action_output
        ] if self.pool else []

    def get_graph_summary(self) -> dict:
        """Get a summary of the current graph state."""
        if not self.graph:
            return {"total_nodes": 0, "active_nodes": 0, "total_edges": 0, "node_types": {}}
        types: dict[str, int] = {}
        for node in self.graph._nodes.values():
            types[node.type.value] = types.get(node.type.value, 0) + 1
        return {
            "total_nodes": self.graph.node_count,
            "active_nodes": self.graph.active_count,
            "total_edges": len(self.graph._edges),
            "node_types": types,
        }

    # ═══════════════════════════════════════════════════════════
    # 图导入导出
    # ═══════════════════════════════════════════════════════════

    def export_graph(self, format: str = "dict") -> dict:
        """Export the graph as a dictionary."""
        return self.graph.to_dict()

    def import_graph(self, data: dict):
        """Import a graph from a dictionary, replacing the current graph."""
        self.graph = GoTGraph.from_dict(data)
        # 重建节点池
        self.pool = NodePool()
        for node in self.graph.get_active_nodes():
            self.pool.add(node)
        # 重新绑定 DMN 的图引用
        self._dmn._graph = self.graph
        self._reasoning_loop._graph = self.graph
        self._scheduler._graph = self.graph

    def export_mermaid(self, path: str = "") -> str:
        """Export the graph as a Mermaid diagram.

        Args:
            path: Optional file path to write the Mermaid output.

        Returns:
            The Mermaid diagram string.
        """
        mermaid = self.graph.to_mermaid()
        if path:
            with open(path, "w", encoding="utf-8") as f:
                f.write(mermaid)
        return mermaid

