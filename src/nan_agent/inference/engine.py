"""
GoT 推理引擎 (Graph of Thought Engine)
----------------------------------------
NAN-Agent 的核心推理引擎，将 Graph of Thought 推理系统的所有组件
整合为一个可运行的推理循环。

组件架构：
    GoTEngine
    ├── GoTGraph          — 推理图数据结构
    ├── NodePool          — 活跃节点池
    ├── DMNGenerator      — 默认模式网络（空闲时自发思考）
    ├── GoTScheduler      — 批次调度器（并行处理节点）
    ├── ReasoningLoop     — 推理循环（分析→思考→元认知→决策分支）
    ├── MetaCognition     — 元认知（质量验证、矛盾检测）
    ├── GoTToolkit        — 工具集（代码执行、搜索能力）
    ├── DrawOfThought     — 画图思考（可视化推理）
    └── TA 集成           — 与 TaskAgent 的双向通信

生命周期模型：
    引擎以统一的 _lifecycle 循环运行，不再区分"后台模式"和"前台模式"。
    CEN（外部输入）只是另一种输入源，与感知、工具返回、DMN 自发生成
    处于同等地位——无需显式模式切换。

    每个 tick：
    1. 消费外部输入（CEN、感知、工具返回）
    2. 若节点池非空，调度器处理批次
    3. 若节点池为空，DMN 自发生成节点
    4. 扩散激活传播 + 全局衰减
    5. 周期性任务（自动保存、记忆反思）
    6. 等待 tick 间隔

持久化：
    图可通过 save_graph / load_graph 序列化到 JSON 文件，
    并支持自动保存（auto_save_interval）和启停时自动加载/保存。

主要入口：
    engine.start()          — 启动后台推理循环
    engine.run_loop()       — 运行主推理循环（阻塞，向后兼容）
    engine.step()           — 执行单步推理
    engine.create_task_node() — 从外部注入任务节点
    engine.save_graph()     — 保存图到文件
    engine.load_graph()     — 从文件加载图
"""

